package org.example.polignos;

public class MinimoPoligno {

    private  double l3, l2, l1;


    public MinimoPoligno(double l3, double l2, double l1) {
        this.l3 = l3;
        this.l2 = l2;
        this.l1 = l1;
    }

    public double getL3() {
        return l3;
    }

    public double getL2() {
        return l2;
    }

    public double getL1() {
        return l1;
    }

    public String getTipo(){
        return getTipo();
    }
}
